<template>
  <section class="hero is-success is-fullheight">
    <div class="hero-body hero-body-mt">
      <div class="container has-text-centered">
        <div class="column is-4 is-offset-4">
          <h3 class="title has-text-grey">Iniciar sesion</h3>
          <p class="subtitle has-text-grey">Por favor inicie sesion</p>
          <div class="box">
            <figure class="avatar">
                <img src="https://placehold.it/128x128">
            </figure>
            <form>
              <div class="field">
                <div class="control">
                  <input  v-model="form.email"
                          @blur="$v.form.email.$touch()"
                          class="input is-large"
                          type="email"
                          placeholder="Correo"
                          autofocus=""
                          autocomplete="email">
                  <div v-if="$v.form.email.$error" class="form-error">
                    <span v-if="!$v.form.email.required" class="help is-danger">Correo es necesario</span>
                    <span v-if="!$v.form.email.email" class="help is-danger">Correo no valido</span>
                  </div>
                </div>
              </div>
              <div class="field">
                <div class="control">
                  <input  v-model="form.password"
                          @blur="$v.form.password.$touch()"
                          class="input is-large"
                          type="password"
                          placeholder="Contraseña"
                          autocomplete="current-password">
                  <div v-if="$v.form.password.$error" class="form-error">
                    <span v-if="!$v.form.password.required" class="help is-danger">Contraseña es necesario</span>
                  </div>
                </div>
              </div>
              <button @click.prevent="login"
                      :disabled="isFormInvalid"
                      class="button is-block btn-yellow is-large is-fullwidth">Login</button>
            </form>
          </div>
          <p class="has-text-grey">
            <a class="link-grey">Iniciar sesion con google</a> &nbsp;·&nbsp;
            <a class="link-grey">Registro</a> &nbsp;·&nbsp;
            <a class="link-grey" href="#">Necesitas ayuda?</a>
          </p>
        </div>
      </div>
    </div>
  </section>
</template>

<script>
import { required, email } from 'vuelidate/lib/validators'
  export default {
    data () {
      return {
        form: {
          email: null,
          password: null
        }
      }
    },
    validations: {
      form: {
        email: {
          required,
          email
        },
        password: {
          required
        }
      }
    },
    computed: {
      isFormInvalid () {
        return this.$v.form.$invalid
      }
    },
    methods: {
      login () {
        this.$v.form.$touch()
        this.$store.dispatch('auth/loginWithEmailAndPassword', this.form)
          .then(() => this.$router.push('/'))
          .catch((err) => console.log(err))
      }
    }
  }
</script>

